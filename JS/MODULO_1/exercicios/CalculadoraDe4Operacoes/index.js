const x = parseFloat(prompt('Insira o primeiro número: '))
const y = parseFloat(prompt('Insira o segundo número: '))

const soma = x + y
const subtracao = x - y
const multiplicacao = x * y
const divisao = x / y

alert(
  'Resultado:\n' +
    '\nSoma: ' +
    soma +
    '\nSubtração: ' +
    subtracao +
    '\nMultitplicação: ' +
    multiplicacao +
    '\nDivisao: ' +
    divisao
)
